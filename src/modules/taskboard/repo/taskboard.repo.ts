/**
 * Drop-in logic for status-binding update:
 * - TaskBoardItem has NO `id` field (composite key is taskBoardId+documentId)
 * - Rebind items to columns BEFORE deleting old columns to avoid FK violations
 */
export async function updateStatusBindingForTaskboardTx(
  tx: Prisma.TransactionClient,
  args: {
    taskBoardId: string;
    statusPropertyId: string;
  },
): Promise<void> {
  const { taskBoardId, statusPropertyId } = args;

  // 1) Update binding
  await tx.taskBoardBindings.update({
    where: { taskBoardId },
    data: { statusPropertyId },
  });

  // 2) Load options for the selected status property
  const options = await tx.propertyOption.findMany({
    where: { propertyId: statusPropertyId },
    select: { id: true, value: true, position: true },
    orderBy: { position: "asc" },
  });

  const optionIds = options.map((o) => o.id);

  // If property has no options, nothing meaningful to build
  if (optionIds.length === 0) return;

  // 3) Ensure TaskBoardColumns exist for all optionIds
  const existingCols = await tx.taskBoardColumn.findMany({
    where: { taskBoardId, optionId: { in: optionIds } },
    select: { id: true, optionId: true },
  });

  const existingOptionIdSet = new Set(existingCols.map((c) => c.optionId));
  const missing = options.filter((o) => !existingOptionIdSet.has(o.id));

  if (missing.length > 0) {
    await tx.taskBoardColumn.createMany({
      data: missing.map((o, idx) => ({
        taskBoardId,
        optionId: o.id,
        label: o.value, // default label = option value
        position: o.position ?? idx,
      })),
      skipDuplicates: true,
    });
  }

  // Re-fetch columns to build a definitive optionId -> columnId map
  const cols = await tx.taskBoardColumn.findMany({
    where: { taskBoardId, optionId: { in: optionIds } },
    select: { id: true, optionId: true },
  });

  const optionIdToColumnId = new Map<string, string>();
  for (const c of cols) optionIdToColumnId.set(c.optionId, c.id);

  // Choose a safe default column for docs that don't have this property set
  const defaultColumnId = cols[0]?.id;
  if (!defaultColumnId) return;

  // 4) Fetch board items (IMPORTANT: no `id` field exists)
  const items = await tx.taskBoardItem.findMany({
    where: { taskBoardId },
    select: {
      documentId: true,
      columnId: true,
    },
  });

  if (items.length === 0) {
    // Still safe to delete unreferenced stale columns
    await tx.taskBoardColumn.deleteMany({
      where: {
        taskBoardId,
        optionId: { notIn: optionIds },
        items: { none: {} }, // prevents FK violation
      },
    });
    return;
  }

  const documentIds = items.map((i) => i.documentId);

  // 5) Read each document's chosen option for the selected statusPropertyId
  const dpvs = await tx.documentPropertyValue.findMany({
    where: {
      documentId: { in: documentIds },
      propertyId: statusPropertyId,
      optionId: { not: null },
    },
    select: {
      documentId: true,
      optionId: true,
    },
  });

  const docIdToOptionId = new Map<string, string>();
  for (const v of dpvs) {
    if (v.optionId) docIdToOptionId.set(v.documentId, v.optionId);
  }

  // 6) Rebind each TaskBoardItem.columnId to the correct column for its option
  for (const item of items) {
    const optionId = docIdToOptionId.get(item.documentId);
    const desiredColumnId =
      (optionId && optionIdToColumnId.get(optionId)) ?? defaultColumnId;

    if (item.columnId !== desiredColumnId) {
      await tx.taskBoardItem.update({
        // composite PK selector generated by Prisma from @@id([taskBoardId, documentId])
        where: {
          taskBoardId_documentId: {
            taskBoardId,
            documentId: item.documentId,
          },
        },
        data: { columnId: desiredColumnId },
      });
    }
  }

  // 7) Now it’s safe to delete stale columns (only ones with NO items)
  await tx.taskBoardColumn.deleteMany({
    where: {
      taskBoardId,
      optionId: { notIn: optionIds },
      items: { none: {} }, // <-- key line to prevent TaskBoardItem_columnId_fkey failure
    },
  });
}

