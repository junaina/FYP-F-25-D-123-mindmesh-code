      // 2) Load the new status property's options
      const options = await tx.propertyOption.findMany({
        where: { propertyId: statusPropertyId },
        orderBy: { position: "asc" },
        select: { id: true },
      });

      if (options.length === 0) {
        throw new Error(`Status property ${statusPropertyId} has no options`);
      }

      const optionIds = options.map((o) => o.id);

      // 3) Ensure columns exist for all options
